def t(headers, rows):
    omitted_indexes = {index for index, header in enumerate(headers) if str(header).strip() in OMITTED_HEADERS}
    if omitted_indexes:
        headers = [header for index, header in enumerate(headers) if index not in omitted_indexes]
        rows = [[value for index, value in enumerate(row) if index not in omitted_indexes] for row in rows]
    return {"type": "table", "headers": headers, "rows": rows}


